# Now it's time to learn some expressions in python
# all expressions:
# x+y: sum of x and y if numbers and concatenated if string.
# x-y: minus y from x.
# x*y: multiply x times y, if one of them is number then it will concatenated same number times another number.
# x/y: divide x by y
# x//y: floor of x divided by y
# x%y: remainder of x divided by y
# x**y: x rais to power y
# examples:
x=5
y=3
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x//y)
print(x%y)
print(x**y)
